export function buildPlayers(n, vertical, oldPlayers = []) {
  const form = vertical ? formV[n] : formH[n];
  const players = [];
  for (let i = 0; i < n; i++) {
    const oldLocal = oldPlayers.find(p => p.team === 0 && p.num === i + 1);
    const oldVisit = oldPlayers.find(p => p.team === 1 && p.num === i + 1);
    players.push({ x: form.local[i][0], y: form.local[i][1], team: 0, num: i + 1, label: oldLocal?.label || '' });
    players.push({ x: form.visit[i][0], y: form.visit[i][1], team: 1, num: i + 1, label: oldVisit?.label || '' });
  }
  return players;
}
